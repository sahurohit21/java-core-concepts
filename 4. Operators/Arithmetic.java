public class Arithmetic {
    public static void main(String[] args) {
        int a = 14, b = 5;
        int a2 = a / b;
        int a3 = a % b;
        char c = 40;
        int d = c - a;
        float f = (float) a / b; // Type_Conversion
        System.out.println(a2);
        System.out.println(a3);
        System.out.println(f);
        System.out.println(d);

        System.out.println(10 + 20 / 4);
    }

}
