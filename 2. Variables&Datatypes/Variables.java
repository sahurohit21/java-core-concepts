import java.lang.*;
class Variables
{
      public static void main(String args[])
      {
           int i = 1899;
           double d = 29.2;
           char c = 'M';
           float f = 12.2f;            // Since, by default decimal floating point no's are DOUBLES 
           System.out.println(f); 
           System.out.println(i);
           System.out.println(d);
           System.out.println(c);
      }



}